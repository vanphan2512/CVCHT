export class AddSolutionModel {
    public WorksSupportId: string = "";
    public User: string = "";
    public SolutionContent: string = "";
    public ListSolutionAttachment: string = "";
    public IsAddNew: boolean;
    constructor(WorksSupportId: string, User: string, SolutionContent: string, ListSolutionAttachment: string, IsAddNew: boolean) {
        this.WorksSupportId = WorksSupportId;
        this.User = User;
        this.SolutionContent = SolutionContent;
        this.ListSolutionAttachment = ListSolutionAttachment;
        this.IsAddNew = IsAddNew;
    }
}