export class EditModel {
    public Id: number = 0;
    public WorksSupportProjectName: string = "";
    public WorksSupportTypeId: number = 0;
    public Description: string = "";
    public OrderIndex: number = 0;
    public IsActived: number = 0;
    public IsSystem: number = 0;
    public IconUrl: string = "";
    public User: string = "";

    constructor(Id: number, WorksSupportProjectName: string,WorksSupportTypeId: number, OrderIndex: number, Description: string,
        IsActived: number, IsSystem: number, IconUrl:string, User: string ) {
        this.Id = Id;
        this.WorksSupportProjectName = WorksSupportProjectName;
        this.WorksSupportTypeId = WorksSupportTypeId;
        this.OrderIndex = OrderIndex; 
        this.Description = Description;       
        this.IsActived = IsActived;
        this.IsSystem = IsSystem;
        this.IconUrl = IconUrl;
        this.User =User;
    }

}